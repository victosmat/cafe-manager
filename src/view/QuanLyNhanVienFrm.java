    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        NhanVien s = new NhanVien(txtID.getText(), txtTenDangNhap.getText(), txtMatKhau.getText(),
                txtTen.getText(),
                txtChucVu.getText(), txtQuanID.getText(), QuanLyNhanVienDAO.getMucLuong(
                        txtChucVu.getText()));
        if (new QuanLyNhanVienDAO().themNhanVien(s)) {
            JOptionPane.showMessageDialog(rootPane, "thêm thành công");
            list.add(s); // them vao danh sach SV
            this.model.addRow(new Object[] {
                    s.getId(), s.getTenDangNhap(), s.getMatKhau(), s.getTen(), s.getChucVu(),
                    s.getQuanID(),
                    s.getMucLuong()
            });
        } else {
            JOptionPane.showMessageDialog(rootPane, "lỗi");
        }
    }// GEN-LAST:event_btnLuuActionPerformed

