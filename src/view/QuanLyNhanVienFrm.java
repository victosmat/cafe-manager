    private void cbxXoaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_cbxXoaActionPerformed
        int row = this.tblNhanVien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(rootPane, "cần chọn 1 dòng cần xoá");
        } else {
            list.remove(row);
            list = new QuanLyNhanVienDAO().getListNhanVien();
            String[] cols = { "ID", "tên đăng nhập", "mật khẩu", "tên", "chức vụ", "mã quán" };
            this.model = new DefaultTableModel(cols, 0);
            list.forEach(t -> model.addRow(new Object[] {
                    t.getId(), t.getTenDangNhap(), t.getMatKhau(), t.getTen(), t.getChucVu(), t.getQuanID()
            }));
            this.tblNhanVien.setModel(this.model);
        } // TODO add your handling code here:
    }// GEN-LAST:event_cbxXoaActionPerformed

